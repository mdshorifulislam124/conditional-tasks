let point = 90;

if (point >= 90 && point <= 100){
    console.log("Grade A+");
}
else if (point >= 80 && point <= 89){
    console.log("Grade A");
}
else if (point >= 70 && point <= 79){
    console.log("Grade B")
}
else if (point >= 60 && point <= 69){
    console.log("Grade C");
}
else if (point >= 33 && point <= 59){
    console.log("Grade D");
}
else {
    console.log("Grade F")
}